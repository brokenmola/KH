package travel.db;

public class travel {

	private int travel_no;
	private String travel_name;
	private String travel_content;
	private String travel_address;
	private int travel_type;
	private int travel_tema;
	
	public int getTravel_no() {
		return travel_no;
	}
	public void setTravel_no(int travel_no) {
		this.travel_no = travel_no;
	}
	public String getTravel_name() {
		return travel_name;
	}
	public void setTravel_name(String travel_name) {
		this.travel_name = travel_name;
	}
	public String getTravel_content() {
		return travel_content;
	}
	public void setTravel_content(String travel_content) {
		this.travel_content = travel_content;
	}
	public String getTravel_address() {
		return travel_address;
	}
	public void setTravel_address(String travel_address) {
		this.travel_address = travel_address;
	}
	public int getTravel_type() {
		return travel_type;
	}
	public void setTravel_type(int travel_type) {
		this.travel_type = travel_type;
	}
	public int getTravel_tema() {
		return travel_tema;
	}
	public void setTravel_tema(int travel_tema) {
		this.travel_tema = travel_tema;
	}
	
	
	
}
